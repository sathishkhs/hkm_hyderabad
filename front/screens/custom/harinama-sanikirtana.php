                    <p>Nagar Sankirtan is an august occasion to promote open public connection wiping out the discrimination among people by fortifying the social texture. Nagar Sankirtan can revive and reestablish our imperiled cultural heritage as well as harmony. In the pervasive condition of violence and vengeance, Nagar Sankirtan, by the virtue of its tendency and appeal, sets up a genuine connection of brotherhood and empathy and in addition hoists the religious spirit.</p>
                    <p>The world today has profoundly felt the need of the irreplaceable “Hari Naam Maha-mantra”. The prophecy of Sri Chaitanya Mahaprabhu has turned out to be a fact as expected that “Hari Naam Maha-mantra” will spread to cover all villages and towns of the world one day. Furthermore, humanity today is resonating to the recurrence of the all universal chant breaking the reservations of various communities and cultures.</p>
                    <p>For world peace, Harinama Sankirtana is organized in the lanes of Hubli-Dharwad each fortnight. More than 200 devotees join the immense chant for deliverance and resonate the Holy name. The devotees are altogether submerged in the ecstasy of chanting the Holy Name of Krishna. The Nagar Sankirtan closes with extravagant and delectable Krishna prasadam.</p>
