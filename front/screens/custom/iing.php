                       <div class="about-item">
                         
                           <p>As per the diverse yugas or ages, like Satya-yuga, Treta-yuga, Dvapara-yuga, and Kali-yuga, there are distinctive processes for spiritual development that have been more recommended than others. For instance, meditation was the recommended procedure for Satya-yuga, when people lived longer and could sit in fixation or in samadhi for longer periods. In Treta-yuga it was best to take part in different extravagant rituals, yajna’s or fire sacrifice. In Dvapara-yuga it was best to participate in elaborate worship, with sumptuous offerings to the prayers and Deities and mantras sung to the Deities. In this way, these processes have proceeded down through the ages to some degree or another. But, in Kali-yuga, however despite everything we see these procedures has been used, now it is the chanting of mantras, especially the Hare Krishna maha-mantra, that is the most proficient and recommended procedure of spiritual development in this age.</p>
