                   <p>Srila Prabhupada says: “You can also establish the kirtana hall on the farm and install Gaura Nitai Deities. Gaura Nitai can be established anywhere, and it is simple to worship them simply chant before Their Lordships. He doesn’t mind that you have not decorated very nicely, but if you chant and dance He is very pleased.” (SP Letter to Madhudvisa, Melbourne, 21 April 1976)</p>
                   <p>Followed by the words of SP, we can worship Nitai Gauranga just by chanting Hare Krishna mantra, As a major aspect of various initiatives, HKM-Hyd began a Program called “Intinta Nitai Gauranga” (Lord Nitai Gauranga in Every house). Where in around 170 families who are practising Krishna consciousness genuinely received Nitai Gauranga deities to worship in their homes each day.</p>
