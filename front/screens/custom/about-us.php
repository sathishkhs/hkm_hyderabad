                    </div>
                    <p>Satisfaction to everyone’s mind. The Supreme Personality of Godhead himself clarifies significance and process of divinity worship in the twenty-seventh section of the eleventh canto in Srimad Bhagavatam. There are three assortments of arcana, Deity worship, in light of the original Vedas, the secondary tantras, or an amalgamation of these. The Deity image, the ground, the fire, the sun, the water and the heart of the worshiper are all genuine locations of the presence of the Deity.

                        Srila Prabhupada gives the example frequently that ‘when you put your mails in a box on the street because it is written there U.S. Mail, you know that it is authorized box. And if you put your letters within this box, it will surely reach the destination. The post office will work. So there is no difference between the huge post office building and that small box because it is authorized. Similarly, the difference between idol worship and Deity worship is like that. Unless authorized process is accepted, it is idol worship. That is the general rule. If somebody thinks that “There is a box, red and blue, on the street. Why shall I go to that box? Let me have the similar box at my door and it will be cleared by the postman because it is blue and red,” that will not be so. Because the box which you put at your door, that is not authorized.’</p>
